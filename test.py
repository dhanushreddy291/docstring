def pow(a, b):
    """
    This function takes two arguments, a and b, and returns the value of a raised to the power of b.
    """
    return a**b


def mul(a, b):
    """
    This function takes two arguments, a and b, and returns their product.
    """
    return a * b


def sub(a, b):
    """
    This function takes two arguments, a and b, and returns their difference (a - b).
    """
    return a - b


def mod(a, b):
    """
    This function takes two arguments, a and b, and returns the remainder when a is divided by b.
    """
    return a % b
