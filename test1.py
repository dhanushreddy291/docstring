def pow(a, b):
    """
    Return the value of a raised to the power of b.

    Args:
        a (int): The base number.
        b (int): The exponent number.

    Returns:
        int: The result of a raised to the power of b.

    """
    return a**b


def mul(a, b):
    """
    Return the product of two numbers.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The product of a and b.
    """
    return a * b


def sub(a, b):
    """
    Return the difference between two numbers.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The difference between a and b.
    """
    return a - b


def mod(a, b):
    """
    Return the remainder of a division operation.

    Args:
        a (int): The dividend.
        b (int): The divisor.

    Returns:
        int: The remainder of a divided by b.
    """
    return a % b
